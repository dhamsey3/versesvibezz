// Helper function to get a poem by slug
export function getPoem(slug: string) {
  return poems.find((poem) => poem.slug === slug) || null
}

// Helper function to get a poet by slug
export function getPoet(slug: string) {
  return poets.find((poet) => poet.slug === slug) || null
}

// Helper function to get poems by poet
export function getPoemsByPoet(poetSlug: string) {
  return poems.filter((poem) => poem.poetSlug === poetSlug)
}
